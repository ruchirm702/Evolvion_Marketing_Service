public enum LeadStage {
    AWARENESS,  // Early stage - just aware of the product or service
    INTEREST,   // Lead shows interest
    CONSIDERATION,  // Actively considering the product
    EVALUATION,  // Evaluating the product or service
    DECISION,   // Ready to make a decision
    CLOSED      // Lead has converted or been lost
}
